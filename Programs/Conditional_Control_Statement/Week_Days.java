public class Week_Days
{
	public static void main(String args[])
	{
		String Day = "Wednesday";
		
		if( Day == "Sunday" )
		{
			System.out.println("\n Idli");
		}
		else if( Day == "Monday" )
		{
			System.out.println("\n Samosa");
		}
		else if( Day == "Tuesday" )
		{
			System.out.println("\n Shira");
		}
		else if( Day == "Wednesday" )
		{
			System.out.println("\n Dosa");
		}
		else if( Day == "Thursday" )
		{
			System.out.println("\n Poha");
		}
		else if( Day == "Friday" )
		{
			System.out.println("\n Uttappa");
		}
		else
		{
			System.out.println("\n Upma");
		}
	}
}